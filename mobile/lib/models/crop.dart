class Crop {
  final String id;
  final String name;
  final String variety;
  final String quantity;
  final String price;
  final String status;
  final String icon;

  Crop({
    required this.id,
    required this.name,
    required this.variety,
    required this.quantity,
    required this.price,
    required this.status,
    required this.icon,
  });
}
